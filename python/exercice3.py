# Saisie des trois nombres
a = int(input("a : "))
b = int(input("b : "))
c = int(input("c : "))

# Comparaison et affichage du plus grand nombre
if a > b & a > c :
    print("a est le plus grand")
elif b > a & b > c :
    print("b est le plus grand")
elif c > a & c > b :
    print("c est le plus grand")
    

  
